from dataclasses import dataclass
from datetime import datetime
from enum import Enum
from psycopg.rows import dict_row
from psycopg import sql
from psycopg import Connection
from .connection import get_pool
import uuid

from typing import Any

# classes


class EpisodeStatus(str, Enum):
    PENDING = "pending"
    PROCESSING = "processing"
    DONE = "done"
    FAILED = "failed"


@dataclass
class NewEpisode:
    title: str
    batch_id: uuid.UUID | None = None
    podcast_id: uuid.UUID | None = None
    audio_path: str | None = None
    xml_path: str | None = None
    published_at: datetime | None = None


@dataclass
class Episode:
    """
    Output Type
    """

    id: uuid.UUID  # generated by db
    batch_id: uuid.UUID
    title: str
    status: EpisodeStatus
    ingested_at: datetime  # generated by db
    updated_at: datetime
    podcast_id: uuid.UUID | None = None
    audio_path: str | None = None
    xml_path: str | None = None
    transcript_path: str | None = None
    published_at: datetime | None = None


ALLOWED_UPDATE_FIELDS = {
    "title",
    "batch_id",
    "podcast_id",
    "audio_path",
    "xml_path",
    "transcript_path",
    "published_at",
    "status",
    "updated_at",  # comment out if postgreSQL trigger is added
}

# helpers


# row["id"] returns None if if empty, row.get() raises Key Error/sql
def _row_to_episode(row: dict[str, Any]) -> Episode:
    return Episode(
        id=row["id"],
        batch_id=row["batch_id"],
        title=row["title"],
        status=EpisodeStatus(row["status"]),
        ingested_at=row["ingested_at"],
        updated_at=row["updated_at"],
        podcast_id=row.get("podcast_id"),
        audio_path=row.get("audio_path"),
        xml_path=row.get("xml_path"),
        transcript_path=row.get("transcript_path"),
        published_at=row.get("published_at"),
    )


def get(episode_id: uuid.UUID, conn: Connection | None = None) -> Episode | None:
    if conn:
        return _execute_get(episode_id, conn)
    with get_pool().connection() as c:
        return _execute_get(episode_id, c)


def _execute_get(episode_id: uuid.UUID, conn: Connection) -> Episode | None:
    with conn.cursor(row_factory=dict_row) as cur:
        cur.execute("SELECT * FROM episodes WHERE id = %s", (episode_id,))
        row = cur.fetchone()
        return _row_to_episode(row) if row else None


def save(episode: NewEpisode, conn: Connection | None = None) -> uuid.UUID:
    if conn:
        return _execute_save(episode, conn)
    with get_pool().connection() as c:
        return _execute_save(episode, c)


def _execute_save(episode: NewEpisode, conn: Connection) -> uuid.UUID:
    """
    Saves a NewEpisode instance to the DB
    return: episode id
    """
    with conn.cursor() as cur:
        cur.execute(
            """
            INSERT INTO episodes (title, batch_id, podcast_id, audio_path, xml_path, published_at)
            VALUES (%s, %s, %s, %s, %s, %s)
            RETURNING id
            """,
            (
                episode.title,
                episode.batch_id,
                episode.podcast_id,
                episode.audio_path,
                episode.xml_path,
                episode.published_at,
            ),
        )
        row = cur.fetchone()
        if not row:
            raise RuntimeError("INSERT returned no id")
        return row[0]


def update(
    episode_id: uuid.UUID, fields: dict[str, Any], conn: Connection | None = None
) -> None:
    if conn:
        return _execute_update(episode_id, fields, conn)
    with get_pool().connection() as c:
        return _execute_update(episode_id, fields, c)


def _execute_update(
    episode_id: uuid.UUID, fields: dict[str, Any], conn: Connection
) -> None:
    if not fields:
        return

    invalid = fields.keys() - ALLOWED_UPDATE_FIELDS
    if invalid:
        raise ValueError(f"Invalid update fields: {invalid}")

    assignments = sql.SQL(", ").join(
        sql.SQL("{} = %s").format(sql.Identifier(key)) for key in fields
    )

    query = sql.SQL("UPDATE episodes SET {} WHERE id = %s").format(assignments)
    values = list(fields.values()) + [episode_id]

    with conn.cursor() as cur:
        cur.execute(query, values)


def get_list(
    podcast_id: uuid.UUID | None = None,
    status: EpisodeStatus | None = None,
    batch_id: uuid.UUID | None = None,
    limit: int = 20,
    offset: int = 0,
    conn: Connection | None = None,
) -> list[Episode]:
    if conn:
        return _execute_get_list(conn, podcast_id, status, batch_id, limit, offset)
    with get_pool().connection() as c:
        return _execute_get_list(c, podcast_id, status, batch_id, limit, offset)


def _execute_get_list(
    conn: Connection,
    podcast_id: uuid.UUID | None = None,
    status: EpisodeStatus | None = None,
    batch_id: uuid.UUID | None = None,
    limit: int = 20,
    offset: int = 0,
) -> list[Episode]:
    conditions = []
    values: list[Any] = []

    if podcast_id is not None:
        conditions.append(sql.SQL("podcast_id = %s"))
        values.append(podcast_id)

    if status is not None:
        conditions.append(sql.SQL("status = %s"))
        values.append(status.value)

    if batch_id is not None:
        conditions.append(sql.SQL("batch_id = %s"))
        values.append(batch_id)

    where = sql.SQL("")
    if conditions:
        where = sql.SQL("WHERE ") + sql.SQL(" AND ").join(conditions)

    values += [limit, offset]

    query = sql.SQL(
        "SELECT * FROM episodes {} ORDER BY ingested_at DESC LIMIT %s OFFSET %s"
    ).format(where)
    with conn.cursor(row_factory=dict_row) as cur:
        cur.execute(query, values)
        return [_row_to_episode(row) for row in cur.fetchall()]
